#  მომხმარებელს შემოატაინეთ 2 რიცხვი, და შემდეგ გამოიყენეთ შედარების ოპერატორები რა რიცხვსაც შეიყვანს მომხმარებელი,
#  მასე გამოიყენთ შედარების ოპერატორები ==,!=,<,>,<=,=>.

first_number = int(input("შეიყვანეთ რიცხვი:"))
second_number = int(input("შეიყვანეთ მე-2 რიცხვი:"))

print(first_number == second_number)
print(first_number != second_number)
print(first_number < second_number)
print(first_number > second_number)
print(first_number <= second_number)
print(first_number >= second_number)