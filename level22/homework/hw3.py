# მომხმარებელს შემოატანინეთ რიცხვი შემდეგ კი ერთიდან ამ რიცხვამდე დაბეჭდეთ ყველა რიცხვის კვადრატის ჯამი

num = int(input("შეიყვანე რიცხვი :"))

sum = 0
for i in range(1, num + 1):  # ციკლი გადის 1 დან num მდე ყველა რიცხვზე
    sum += i ** 2 # ყოველ ნაბიჯზე ითვლის i კვადრატს და ამატებს ამ კვადრატს ჯამში:


print(sum)