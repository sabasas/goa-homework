# შექმენით input  რომელიც მომხმარებელს ეკითხება რიცხვს და შემდეგ while  ციკლის გამოყენებით ი დაიწყებს რიცხვების დაბეჭდვსს სანამ არ მიაღწევს ნულს

num =int(input("შეიყვანეთ რიცხვი:"))
while num >= 0: # ციკლი იმუშავებს სანამ ნამი არის 0 ან მეტი
    print(num) # ყოველი იტერაცია დაბეწდავს num მიმდინარე მნიშვნელობას
    num = num -1 # ყოველი იტერაციის დროს  num მცირდება 1
