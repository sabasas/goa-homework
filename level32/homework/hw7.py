# გააკეთეთ While Loop. ეს loop-ი უნდა გაეშვას 50-ჯერ და ზუსტად 47 loop-ზე უნდა შეჩერდეს loop-ი, თითოეულ loop-ის დატრიალებაზე უნდა დაპრინტოს მერამდენე დატრიალება მოხდა ანუ: Loop 1 Loop 2 Loop 3 და ასე შემდეგ. მინიშნება: შექმენით Counter ცვლადი და მასში შეინახეთ 0 ციფრი ყოველ loop-ის დატრიალებაზე უნდა მოიმატოს ერთით.

num = 0

while num < 50:
    print("loop", num  + 1)
    num += 1
    if num == 47:
        num = 50