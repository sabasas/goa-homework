#  მომხმარბელს შემოატანინეთ რიცხვი შემდეგ კი მომხმარებლის შემოტანილ რიცხვამდე დაბეჭდეთ რიცხვები და გვერძე მიუწერეთ ლუწია თუ კენტი

num = int(input("შეიყვანე რიცხვი:"))
for i in range(num):
    if i % 2 == 0:
        print( str (i) + "." + "კენტია")

    elif i % 2 != 0:
        print(str (i) + "." + "ლუწია")    
         